  var title = 'Курс Dart видео 2';
  title = 'С нуля до профи Dart Переменные #2';
  // String
  String title2 = 'Some string';

  // Var -> int
  var likes = 99999;
  likes = 100000;
  // Int
  int likes2 = 1000;

